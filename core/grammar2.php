			<div class="section-title text-title-small">⠕ 想音動詞</div><br/>
			<p>
				想音動詞とは、単語構築レベルで単語が変化する動詞のこと。
				新約パスタリエでは、動詞はすべからず想音動詞となる為、想音動詞の修得が新約パスタリエ修得の鍵となる。
