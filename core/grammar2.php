			<div class="section-title text-title-small">⠕ 基礎文法</div><br/>
			<p>
				新約パスタリエの文法は非常にシンプルである。それは、この言語がいかに高速に、少ない言葉で最大の効果を
				発揮できるかを追求して作られた言語だからである。
				その為、最短文法では１語から既に文章的な意味を持つことが出来るのである。
			</p>
