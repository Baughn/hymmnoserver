					レベル２：相手と向き合う（相手への想いを表現する）<br/>
					レベル３：この場全体と向き合う（この場所に対しての想いを表現する)
				</p>
				<p>
					それぞれのレベルは、どこに対しての想いかを表現しているのみであり、レベル３の方がレベル１よりも強い、といったような事は無い。
